.MODEL SMALL
.STACK 64
.CODE
START:
        MOV BL, 030H
		MOV BH, 030H
		
NEXT:
		MOV AX, 0003H
		INT 10H
		MOV DL, BH
		MOV AH, 02H
		INT 21H
		MOV DL, BL
		MOV AH, 02H
		INT 21H
		CMP BL, 039H
		JE ITR
		INC BL
		JMP NEXT
		
ITR:
		CMP BH, 039H
		JE PROGRAMEND
		MOV BL, 030H
		INC BH
		JMP NEXT
		        
PROGRAMEND:
        MOV AH, 4CH
        INT 21H

END START
.END
